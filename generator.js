const generateRandomString = () => {
  let shortURL = '';
  const characters = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789';
  for(let i = 0; i < 6; i++) {
    shortURL += characters.charAt(Math.floor(Math.random()* characters.length));
  }
  return shortURL;
};

